
/* You know java python doesn't support float value in switch,
but in dart it works as butter smooth.

*/ 


void main() {

        var x = 1.0;

        switch(x) {

                case 1.0 :
                        print("One");

                case 2.0 :
                        print("Two");

                case 3.0 :
                        print("Three");

                case 4.0 :
                        print("Four");

                default :
                        print("No Match");
        }
}

